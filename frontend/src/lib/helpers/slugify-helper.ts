const slugify = (name: string, options: { lower?: boolean } = {}): string => {
  const map: Record<string, string> = {
    а: 'a',
    б: 'b',
    в: 'v',
    г: 'g',
    д: 'd',
    е: 'e',
    ё: 'e',
    ж: 'zh',
    з: 'z',
    и: 'i',
    й: 'y',
    к: 'k',
    л: 'l',
    м: 'm',
    н: 'n',
    о: 'o',
    п: 'p',
    р: 'r',
    с: 's',
    т: 't',
    у: 'u',
    ф: 'f',
    х: 'h',
    ц: 'ts',
    ч: 'ch',
    ш: 'sh',
    щ: 'sch',
    ъ: '',
    ы: 'y',
    ь: '',
    э: 'e',
    ю: 'yu',
    я: 'ya',
  };

  let slug = name
    .split('')
    .map((char) => {
      const lower = char.toLowerCase();
      const translit = map[lower];
      if (!translit) return char;
      return char === lower ? translit : translit.charAt(0).toUpperCase() + translit.slice(1);
    })
    .join('')
    .replace(/[^a-zA-Z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-');

  if (options?.lower) slug = slug.toLowerCase();

  return slug;
};

export { slugify };
